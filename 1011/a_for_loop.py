#This will iterate every single characters.
for item in 'Python':
    print(item)
#can be a list of customers, emails, products or etc
for item in ['mosh', 'john', 'sara']:
    print(item)
#range function iterates til givien data. Zero is the default
for item in range(10):
    print(item)
#start from & to
for item in range(5, 10):
    print(item)
#start from the first then add the third data til meeting the secend data
for item in range(5, 10, 4):
    print(item)